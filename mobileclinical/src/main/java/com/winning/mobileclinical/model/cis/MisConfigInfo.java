package com.winning.mobileclinical.model.cis;

public class MisConfigInfo {
	public String id;
    public String moduleid;
    public String modulename;
    public String functypeid;
    public String functypename;
    public String funcid;
    public String funcname;
    public String configvalue;
    public String externalvalue;
    public String memo;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getModuleid() {
		return moduleid;
	}
	public void setModuleid(String moduleid) {
		this.moduleid = moduleid;
	}
	public String getModulename() {
		return modulename;
	}
	public void setModulename(String modulename) {
		this.modulename = modulename;
	}
	public String getFunctypeid() {
		return functypeid;
	}
	public void setFunctypeid(String functypeid) {
		this.functypeid = functypeid;
	}
	public String getFunctypename() {
		return functypename;
	}
	public void setFunctypename(String functypename) {
		this.functypename = functypename;
	}
	public String getFuncid() {
		return funcid;
	}
	public void setFuncid(String funcid) {
		this.funcid = funcid;
	}
	public String getFuncname() {
		return funcname;
	}
	public void setFuncname(String funcname) {
		this.funcname = funcname;
	}
	public String getConfigvalue() {
		return configvalue;
	}
	public void setConfigvalue(String configvalue) {
		this.configvalue = configvalue;
	}
	public String getExternalvalue() {
		return externalvalue;
	}
	public void setExternalvalue(String externalvalue) {
		this.externalvalue = externalvalue;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
}
